package pprraaccttiiccee;

public class Swapping {
	
	public static void main (String[] args) {
		
		int a = 20;
		int b = 30;
		
		a = a + b;
		b = a - b;
		a = a - b;
		
		System.out.print("a = " + a + " , " + "b = " + b);
		System.out.println();
		
		
		int c ;
		int d = 100 ;
		int e = 200 ;
		
		c = d;
		d = e;
		e = c;
		
		System.out.print("d = " + d + " , " + "e = " + e);
		
	}

}
