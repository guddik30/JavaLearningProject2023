package loops;

public class EvenNoUsingDoWhile {
	
	public static void main(String[] args) {
		System.out.println("Even No Using Do While Loop : ");
	
		int x = 0;
	do
	{ if(x%2==0) {
				System.out.print(x + " ");}
			x++; }
	while(x<=50);
		
		
		System.out.println("\n \nOdd No Using Do While Loop : ");
	
		
		int y = 50;
		do
		 {
		 if (y%2==0) {
			System.out.print(y + " ");}
		y--; }
		while(y>=0);
		}
	}



