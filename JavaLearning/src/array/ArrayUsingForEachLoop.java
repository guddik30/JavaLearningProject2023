package array;

public class ArrayUsingForEachLoop {
	
	public static void main(String[] args) {
		
		String z [] = {"av", "bb", "cc"} ;
		
		for(String x:z)
			System.out.println(x);
		
		System.out.println();
		
		int [] a = {44,67,6,};
		
		for (int b : a)
		  System.out.println(b);

}
}